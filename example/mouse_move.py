import pyautogui  # 마우스 제어를 위한 pyautogui 모듈을 불러옵니다
import random     # 랜덤 위치를 생성하기 위해 random 모듈을 불러옵니다
import time       # 시간 지연을 위해 time 모듈을 불러옵니다

# 1. 화면의 크기를 가져옵니다.
screen_width, screen_height = pyautogui.size()  # screen_width와 screen_height에 화면 크기 할당

# 2. 마우스를 랜덤하게 움직이면서 클릭하는 함수를 정의합니다.
def random_mouse_movement_click():
    # 무한 루프로 계속 랜덤 움직임과 클릭을 반복합니다
    # while True:

    # 5번 반복하도록 for 루프를 설정합니다
    for _ in range(5):  # _는 반복 횟수에 관계없이 반복만 필요할 때 사용합니다
        # 화면 안에서 랜덤한 x, y 좌표를 생성합니다
        x = random.randint(0, screen_width)    # x 좌표를 0부터 화면 너비까지 랜덤하게 설정
        y = random.randint(0, screen_height)   # y 좌표를 0부터 화면 높이까지 랜덤하게 설정

        # 마우스를 랜덤한 좌표로 이동합니다
        pyautogui.moveTo(x, y, duration=0.5)   # 이동하는 데 0.5초 소요되도록 설정

        # 현재 위치에서 클릭합니다
        pyautogui.click()                      # 이동한 위치에서 마우스를 클릭

        # 잠깐 멈춤으로써 움직임이 너무 빠르지 않게 합니다
        time.sleep(1)                          # 1초 대기 후 다음 움직임 실행

# 3. 함수를 실행합니다
random_mouse_movement_click()  # 마우스가 랜덤하게 움직이면서 클릭하도록 함수를 호출
